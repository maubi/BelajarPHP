<?php

// Perulangan

// for
// while
// do while

// 1. for
for ($i = 0; $i <= 10; $i++){
    echo "Angka ke- $i <br>";
}

// 2. while
$k = 0;
while ($k <= 10){
    echo "Bilangan ke-$k <br>";
    $k++;
}

// 3. do while
$x = 0;
do {
    echo "Bilangan ke-$x <br>";
    $x++;
} while ($x <= 10);

?>